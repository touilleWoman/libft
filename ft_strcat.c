#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		x;
	int		y;

	x = (int)ft_strlen(s1);
	y = 0;
	while (s2[y] != 0)
	{
		s1[x] = s2[y];
		x++;
		y++;
	}
	s1[x] = 0;
	return (s1);
}
